import React, { useState, useEffect } from "react";

interface AsciiProps {
  fps?: number;
  color?: string;
  className?: string;
  scale?: number;
}

/* ─── Generic Player ─── */
export const AsciiPlayer = ({
  frames, fps = 4, color = "var(--accent)", className = "",
}: { frames: string[][], fps?: number, color?: string, className?: string }) => {
  const [idx, setIdx] = useState(0);
  useEffect(() => {
    const i = setInterval(() => setIdx(f => (f + 1) % frames.length), 1000 / fps);
    return () => clearInterval(i);
  }, [frames, fps]);
  return (
    <pre
      className={`select-none whitespace-pre leading-[1.15] font-mono ${className}`}
      style={{ color, textShadow: `0 0 10px ${color}99`, fontSize: "inherit" }}
    >
      {frames[idx].join("\n")}
    </pre>
  );
};

